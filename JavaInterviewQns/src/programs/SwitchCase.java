package programs;

public class SwitchCase {

	
	public static void main(String[] args) {
		
		int i=2;
		
		
		
		switch(i) {
		
		case 1:
			System.out.println("This is case1 value");
			
		case 2:
			System.out.println("This is case2 value");
			
		case 3:
			default:
				System.out.println("This is a default value");
			
		}
	}
}
